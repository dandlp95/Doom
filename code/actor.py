class Actor:
    
    def  __init__(self):
        self._img = ""
        self._x = ""
        self._y = ""
        self._x_change = ""
        self._y_change = ""
        
    def set_img(self, img):
        self._img = img
    
    def set_x(self, x):
        self._x = x
    
    def set_y(self, y):
        self._y = y
    
    def set_x_change(self, x_change):
        self._x_change = x_change
    
    def set_y_change(self, y_change):
        self._y_change = y_change
        
    def get_img(self):
        return self._img
    
    def get_x(self):
        return self._x

    def get_y(self):
        return self._y
    
    def get_x_change(self):
        return self._x_change

    def get_y_change(self):
        return self._y_change
    
    
        
    
    
    
    
        
        